export const randomFrom = (arr) => arr[Math.floor(Math.random() * arr.length)]

export const generateLocation = (ethnicityKey) => {
  const loc = ETHNICITY_CITIES[ethnicityKey]
  if (!loc) return { city: 'Unknown', country: 'Unknown' }
  return { city: randomFrom(loc.cities), country: loc.country }
}

export const generateName = (weightClass, gender) => {
  const divisionList = DIVISION_ETHNICITIES[gender]?.[weightClass]
  const fallbackKey = gender === 'women' ? 'women_american' : 'american_black'
  const ethnicityKey = divisionList ? randomFrom(divisionList) : fallbackKey
  const ethnicity = ETHNICITIES[ethnicityKey] || ETHNICITIES[fallbackKey]
  const { city, country } = generateLocation(ethnicityKey)
  return {
    name: `${randomFrom(ethnicity.firstNames)} ${randomFrom(ethnicity.lastNames)}`,
    flag: ethnicity.flag,
    nationality: ethnicity.nationality,
    city,
    country
  }
}