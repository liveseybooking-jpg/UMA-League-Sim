export const randomFrom = (arr) => arr[Math.floor(Math.random() * arr.length)]

export const generateName = (weightClass, gender) => {
  const divisionList = DIVISION_ETHNICITIES[gender][weightClass]
  const ethnicityKey = randomFrom(divisionList)
  const ethnicity = ETHNICITIES[ethnicityKey] || ETHNICITIES['american_black']
  const firstName = randomFrom(ethnicity.firstNames)
  const lastName = randomFrom(ethnicity.lastNames)
  return {
    name: `${firstName} ${lastName}`,
    flag: ethnicity.flag,
    nationality: ethnicity.nationality
  }
}